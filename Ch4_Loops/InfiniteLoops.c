#include<stdio.h>

int main(void){

    //using for loop 
    for (;;){
        printf("infinite loop using for loop\n");
    }

    //using do-while loop
    do {
        printf("infinite loop using for do-while loop\n");
    }while(1);


    //using while loop 
    while (1){
        printf("infinite loop using while loop\n");
    }
    return 0 ;
}